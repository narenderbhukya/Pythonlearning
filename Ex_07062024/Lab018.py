
# Raw string
dir = ' C:\nsd\dell dir'  # it prints the string
print(dir)

# adding r''  -- for directory
dir = r'C:\user\dell dir'  # printing the exact directory
print(dir)

# Formate of the string
# f --- formatting -- it will replace the variable name with its value in print statement
# {} -- are placeholders
first_name = "Tom and "
second_name = " Jerry"
print(f'Your name is {first_name}{second_name}')
