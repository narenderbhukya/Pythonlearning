# data types
# integer -- all the whole numbers with zero
# exm -- 1 , 5, 100 , -10 , -2 , 0

age = 25
print(age)
print(type(age))

# string -- all the name and sentences with in the double quotes " " or single quote ' ' or triple quote """ """
# exm -- "Name" , "Hello", "good" , "12345" , "12.5" , "true"

name = "Rohit sharma"
print(name)
print(type(name))

# float -- all the number with fractional or decimal values with positive and negative
# exm --- 12.4 ,1.4 , -20.3, -9.0

height = 1.78
print(height)
print(type(height))

# boolean -- true or false
# exm -- True , False
is_correct = True
print(is_correct)
print(type(is_correct))

complex_number = 2+5j
print(complex_number.real)
print(complex_number.imag)




